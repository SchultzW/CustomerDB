package classes;

public class state 
{
	private String StateCode;
	private String StateName;
	public state() {}
	
	public String getStateCode()
	{
		return StateCode;
	}
	public void setStateCode(String code)
	{
		this.StateCode=code;
	}
	public String getStateName()
	{
		return StateName;
	}
	public void setStateName(String name)
	{
		this.StateName=name;
	}
	
}
